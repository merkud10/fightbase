async function main() {
  let updated = 0;

  for (const [slug, body] of Object.entries(overrides)) {
    const article = await prisma.article.findUnique({
      where: { slug },
      include: { sections: { orderBy: { sortOrder: "asc" } } }
    });

    if (!article || article.sections.length === 0) {
      continue;
    }

    await prisma.articleSection.update({
      where: { id: article.sections[0].id },
      data: { body }
    });

    updated += 1;
  }

  console.log(`Overrode bodies for ${updated} articles`);
}

main()
  .catch((error) => {
    console.error(error);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
